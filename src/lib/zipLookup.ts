export interface ZipResult {
  city: string
  state: string
}

// Lazy-load the zipcodes database (4MB+) only when first lookup is requested
let zipcodesModule: typeof import('zipcodes') | null = null

/**
 * Look up city and state for a US 5-digit ZIP code.
 * Lazy-loads the zipcodes database on first call.
 * Returns null if the ZIP is not found.
 */
export async function lookupZip(zip: string): Promise<ZipResult | null> {
  if (!zipcodesModule) {
    zipcodesModule = await import('zipcodes')
  }
  const result = zipcodesModule.lookup(zip)
  if (!result) return null
  return { city: result.city, state: result.state }
}

